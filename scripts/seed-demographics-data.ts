async function seedDemographicsData() {
  console.log("üå± Starting demographics data seed...");

  try {
    // Clear existing data
    console.log("üóëÔ∏è  Clearing existing demographics data...");
    await prisma.demographicsCounty.deleteMany({});
    await prisma.demographicsProvince.deleteMany({});
    await prisma.demographicsReportSummary.deleteMany({});

    // Create provinces
    console.log("üìç Creating provinces...");
    const createdProvinces: Record<string, string> = {};
    for (const province of provincesData) {
      const created = await prisma.demographicsProvince.create({
        data: province,
      });
      createdProvinces[province.name] = created.id;
      console.log(`  ‚úì Created province: ${province.name}`);
    }

    // Create counties
    console.log("üèòÔ∏è  Creating counties...");
    for (const county of countiesData) {
      const provinceId = createdProvinces[county.province];
      if (!provinceId) {
        console.error(`  ‚úó Province not found for county: ${county.name}`);
        continue;
      }

      await prisma.demographicsCounty.create({
        data: {
          name: county.name,
          provinceId,
          displayOrder: county.displayOrder,
          clubsGAA: county.clubsGAA,
          clubsLGFA: county.clubsLGFA,
          clubsCamogie: county.clubsCamogie,
          youthMalePlayers: county.youthMalePlayers,
          youthFemalePlayers: county.youthFemalePlayers,
          fullMalePlayers: county.fullMalePlayers,
          fullFemalePlayers: county.fullFemalePlayers,
          nonPlayers: county.nonPlayers,
          totalPitches: county.totalPitches,
          totalFloodlitPitches: county.totalFloodlitPitches,
          totalFullSizePitches: county.totalFullSizePitches,
          teamsPerPitch: county.teamsPerPitch,
          teamsCamogie: county.teamsCamogie,
          teamsLadiesFootball: county.teamsLadiesFootball,
          teamsHurling: county.teamsHurling,
          teamsFootball: county.teamsFootball,
          totalTeams: county.totalTeams,
          populationFemale: county.populationFemale,
          populationMale: county.populationMale,
          populationTotal: county.populationTotal,
          averageAge: county.averageAge,
          population0to5: county.population0to5,
          population6to11: county.population6to11,
          reportYear: 2025,
          censusYear: "2021/2022",
        },
      });
      console.log(`  ‚úì Created county: ${county.name} (${county.province})`);
    }

    // Update province totals
    console.log("üìä Calculating province totals...");
    for (const provinceName of Object.keys(createdProvinces)) {
      const counties = await prisma.demographicsCounty.findMany({
        where: { provinceId: createdProvinces[provinceName] },
      });

      const totals = counties.reduce(
        (acc, county) => ({
          totalPopulation: acc.totalPopulation + (county.populationTotal || 0),
          totalClubsGAA: acc.totalClubsGAA + (county.clubsGAA || 0),
          totalClubsLGFA: acc.totalClubsLGFA + (county.clubsLGFA || 0),
          totalClubsCamogie: acc.totalClubsCamogie + (county.clubsCamogie || 0),
          totalPitches: acc.totalPitches + (county.totalPitches || 0),
          totalYouthMalePlayers:
            acc.totalYouthMalePlayers + (county.youthMalePlayers || 0),
          totalYouthFemalePlayers:
            acc.totalYouthFemalePlayers + (county.youthFemalePlayers || 0),
          totalFullMalePlayers:
            acc.totalFullMalePlayers + (county.fullMalePlayers || 0),
          totalFullFemalePlayers:
            acc.totalFullFemalePlayers + (county.fullFemalePlayers || 0),
          totalNonPlayers: acc.totalNonPlayers + (county.nonPlayers || 0),
          totalMembers:
            acc.totalMembers +
            (county.youthMalePlayers || 0) +
            (county.youthFemalePlayers || 0) +
            (county.fullMalePlayers || 0) +
            (county.fullFemalePlayers || 0) +
            (county.nonPlayers || 0),
        }),
        {
          totalPopulation: 0,
          totalClubsGAA: 0,
          totalClubsLGFA: 0,
          totalClubsCamogie: 0,
          totalPitches: 0,
          totalYouthMalePlayers: 0,
          totalYouthFemalePlayers: 0,
          totalFullMalePlayers: 0,
          totalFullFemalePlayers: 0,
          totalNonPlayers: 0,
          totalMembers: 0,
        }
      );

      await prisma.demographicsProvince.update({
        where: { id: createdProvinces[provinceName] },
        data: {
          totalPopulation: totals.totalPopulation,
          totalClubsGAA: totals.totalClubsGAA,
          totalClubsLGFA: totals.totalClubsLGFA,
          totalClubsCamogie: totals.totalClubsCamogie,
          totalPitches: totals.totalPitches,
          totalYouthMalePlayers: totals.totalYouthMalePlayers,
          totalYouthFemalePlayers: totals.totalYouthFemalePlayers,
          totalFullMalePlayers: totals.totalFullMalePlayers,
          totalFullFemalePlayers: totals.totalFullFemalePlayers,
          totalNonPlayers: totals.totalNonPlayers,
          totalMembers: totals.totalMembers,
        },
      });
      console.log(`  ‚úì Updated totals for ${provinceName}`);
    }

    // Create report summary
    console.log("üìã Creating report summary...");
    await prisma.demographicsReportSummary.create({
      data: reportSummary,
    });
    console.log("  ‚úì Created report summary");

    console.log("\n‚úÖ Demographics data seed completed successfully!");
    console.log(`   - 4 provinces created`);
    console.log(`   - ${countiesData.length} counties created`);
    console.log(`   - 1 report summary created`);
  } catch (error) {
    console.error("‚ùå Error seeding demographics data:", error);
    throw error;
  } finally {
    await prisma.$disconnect();
  }
}

seedDemographicsData();
