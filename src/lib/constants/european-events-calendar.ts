// Get events by country
export function getEventsByCountry(countryCode: string): EuropeanEvent[] {
  return allEuropeanEvents.filter((e) => e.countryCode === countryCode);
}

// Get events in date range
export function getEventsInRange(
  startDate: Date,
  endDate: Date
): EuropeanEvent[] {
  return allEuropeanEvents.filter((e) => {
    const eventDate = new Date(e.date);
    return eventDate >= startDate && eventDate <= endDate;
  });
}

// Get high appeal events only
export function getHighAppealEvents(): EuropeanEvent[] {
  return allEuropeanEvents.filter((e) => e.travelAppeal === "high");
}

