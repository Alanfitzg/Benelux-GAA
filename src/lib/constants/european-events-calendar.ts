/**
 * European Events Calendar for Event Builder
 * Includes national holidays, religious holidays, and major cultural festivals
 * These represent prime opportunities for GAA tournaments
 */

export interface EuropeanEvent {
  date: string; // ISO date YYYY-MM-DD (for fixed dates) or "variable" for moving dates
  name: string;
  country: string;
  countryCode: string;
  type: "national" | "religious" | "festival" | "cultural";
  description: string;
  travelAppeal: "high" | "medium" | "low"; // How attractive for GAA travel
  typicalDuration: number; // Days
  wikiUrl?: string; // Wikipedia link for more info
}

// Countries that have GAA clubs (based on database)
export const GAA_CLUB_COUNTRIES = [
  "ES", // Spain - 25 clubs
  "FR", // France - 22 clubs
  "DE", // Germany - 11 clubs
  "NL", // Netherlands - 6 clubs
  "SE", // Sweden - 5 clubs
  "PL", // Poland - 4 clubs
  "CH", // Switzerland - 4 clubs
  "DK", // Denmark - 4 clubs
  "FI", // Finland - 3 clubs
  "BE", // Belgium - 3 clubs
  "PT", // Portugal - 2 clubs
  "AT", // Austria - 2 clubs
  "IT", // Italy - 2 clubs
  "NO", // Norway - 2 clubs
  "GI", // Gibraltar - 2 clubs
  "CZ", // Czech Republic - 2 clubs
  "IS", // Iceland - 1 club
  "HR", // Croatia - 1 club
  "EE", // Estonia - 1 club
  "HU", // Hungary - 1 club
  "SK", // Slovakia - 1 club
] as const;

export type GaaClubCountryCode = (typeof GAA_CLUB_COUNTRIES)[number];

export function isGaaClubCountry(countryCode: string): boolean {
  return GAA_CLUB_COUNTRIES.includes(countryCode as GaaClubCountryCode);
}

export function getGaaClubCountryEvents(
  events: EuropeanEvent[]
): EuropeanEvent[] {
  return events.filter((e) => isGaaClubCountry(e.countryCode));
}

